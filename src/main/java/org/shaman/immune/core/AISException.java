package org.shaman.immune.core;

import org.shaman.exceptions.LearnerException;

// *********************************************************\
// *       An Exception generated by the AIS-Engine        *
// *********************************************************/
public class AISException extends LearnerException
{
    public AISException(String message) {
        super(message);
    }
    
    public AISException(Throwable throwable) {
        super(throwable);
    }

    public AISException(String message, Throwable throwable) {
        super(message,throwable);
    }
}
